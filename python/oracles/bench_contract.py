class ExprEdge(_Base):
    """One tied/shared-parameter expression edge.

    Wire form (camelCase): ``{"targetNode": "p1", "targetParam": "sigma",
    "expression": "p0.sigma"}``. Typed (not a bare ``dict[str, str]``) so the
    inner keys camelize like every other contract field — the web reads
    ``edge.targetNode`` / ``edge.targetParam``. Accepts the engine's snake_case
    dicts via ``populate_by_name`` on :class:`_Base`.
    """

    target_node: str
    target_param: str
    expression: str


