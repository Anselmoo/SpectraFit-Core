# ---------------------------------------------------------------------------
# Supported-schema window — the SINGLE SOURCE OF TRUTH for which payload
# versions a current build renders (the web `SUPPORTED_SCHEMA` set is pinned to
# THIS by `tests/unit/benchmark/test_supported_schema_window.py`, which fails if
# the two drift — the named, non-bypassable guard that closes the SP-3-class
# latent-red where the web gate silently lagged a schema bump).
#
# It is a WINDOW, not a single value, on purpose: an older version stays
# accepted while its payloads can still be migrated/rendered, so a transition
# can widen the window here (the canonical source) independently of bumping
# `SCHEMA_VERSION` — preserving the "accept old payloads during a transition"
# stopgap in one authoritative place instead of a hand-edited TS literal.
#
# Invariants (enforced by the guard test): `SCHEMA_VERSION` is in the window,
# and every windowed version has a migration path to `SCHEMA_VERSION` in
# `oracles.migrate.MIGRATIONS` (so the window can never admit an
# un-migrate-able version).
SUPPORTED_SCHEMA: tuple[str, ...] = ("1.4", "1.5", "1.6", "1.7")

SolverId = str  # "spectrafit" | "lmfit" | "jax" (open for future backends)

# ---------------------------------------------------------------------------
# Canonical wire-format strings (Top-10 #7 / Cycle 6).
#
# `ModelTypeStr::as_str()` on the Rust side pinned model-type wire strings via
# a parity test (`model_type_as_str_matches_serde_wire_for_every_variant`).
# This block applies the same pattern to the two remaining cross-cutting
# string-typed wire fields:
#
#   1. Gate state — 3-axis levels for the `spc-bench gate --json` output and
#      (eventually) a manifest field. The CLI writes lowercase
#      `"pass" / "warn" / "fail"` (cli.py lines 218-262); the web binding
#      (`web/src/shell/EvidenceVerdict.tsx`) renders all three states. This
#      parity test guards against wire-format drift between producer
#      (Python) and consumer (TS) — the exact bug class Top-10 #7 was raised
#      to prevent.
#
#   2. Known solver ids — `SolverId` is `str` for forward-compat (new backends
#      register via `BACKEND_REGISTRY`), but the *currently known* roster is
#      a closed set the contract should pin so a typo in a baseline name
#      (`"lmift"` for `"lmfit"`) is caught at validation time, not at
#      benchmark time.
#
# Both fields stay `str` on the wire (so the contract is open for extension)
# but a parity test pins the canonical roster + the legal gate states. New
# backends or gate states are a deliberate one-line registry edit; typos
# are caught by the test.
# ---------------------------------------------------------------------------
GateState = Literal["pass", "warn", "fail"]
"""Three-axis gate state. Wire format = lowercase to match `spc-bench gate
--json` output (`cli.py`). The web binding currently uppercases on render —
that's a presentation decision; the wire stays lowercase."""

GATE_STATES: tuple[GateState, ...] = ("pass", "warn", "fail")
"""Exhaustive ordered roster — `GATE_STATES[GATE_RANK[s]] == s` for every
state. Used by tests + the worst-of aggregator."""

GATE_RANK: dict[GateState, int] = {"pass": 0, "warn": 1, "fail": 2}
"""Worst-of rank: max(rank) wins when aggregating 3-axis gate states into a
single headline. `fail` always wins, then `warn`, then `pass`."""

KNOWN_SOLVER_IDS: frozenset[str] = frozenset(
    {
        "spectrafit",
        "lmfit",
        "jax",
        "scipy-ls-lm",
        "scipy-ls-trf",
        "scipy-ls-dogbox",
    }
)
"""Closed roster of currently registered backend ids. New backends are a
deliberate one-line addition here PLUS a `register_backend(...)` entry in
the bench backends module — see Top-10 #2 (deferred). A new bench run with
an unknown id round-trips through the contract because `SolverId = str`,
but the parity test in `tests/parity/test_canonical_wire_strings.py`
flags the orphan so the maintainer adds it to the roster intentionally."""


class _Base(BaseModel):
    """Base model: serialize to camelCase, accept either snake_case or alias."""

    model_config = ConfigDict(
        alias_generator=to_camel,
        populate_by_name=True,
        extra="forbid",
    )


