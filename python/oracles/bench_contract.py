class PinnedBaseline(_Base):
    """The ``.spectrafit_reports/perf_baseline.json`` sidecar surfaced to the web.

    Mirrors :func:`oracles.reports.write_perf_baseline`'s on-disk shape so
    the gate's self-vs-self signal is one fetch away in the browser.
    """

    run_id: str
    recorded_at: str  # ISO-8601 UTC
    geomean_speedup_vs_baseline: float
    n_cases: int


class ManifestSignals(_Base):
    """Headline numbers from ``manifest.json`` surfaced via the contract.

    Mirrors the keys :func:`oracles.reports._headline` writes; ``pinned``
    is populated from :func:`oracles.reports.read_perf_baseline` (``None``
    when no baseline is currently pinned).
    """

    geomean_speedup_vs_baseline: float
    max_abs_delta_r2: float
    spectrafit_win_rate: float
    regressions: int
    pinned: PinnedBaseline | None = None
    harmonic_mean_speedup_vs_baseline: float | None = Field(
        default=None,
        description=(
            "Harmonic mean of per-case speedup values vs the baseline solver. "
            "Complements the geometric mean per Eeckhout (2024): the harmonic mean "
            "is the correct aggregate for equal-time comparisons and is always ≤ the "
            "geometric mean for positively-skewed speedup distributions. "
            "Additive field — old payloads on disk validate with None."
        ),
    )
    gate_state: GateState | None = Field(
        default=None,
        description=(
            "Aggregate gate verdict. Single source of truth; the web UI must "
            "render this rather than recomputing from regression flags."
        ),
    )
    nonfinite_dr2_case_ids: list[str] = Field(
        default_factory=list,
        description=(
            "Case ids whose |Δr²| vs the baseline was non-finite (NaN/Inf). The "
            "accuracy gate fails when this is non-empty; carried as a list so "
            "_sanitize (which coerces non-finite floats) cannot erase the signal."
        ),
    )
    saturated_categories: list[str] = Field(
        default_factory=list,
        description=(
            "Categories where every supported backend hits r²≥0.999 on every case. "
            "Differential below the noise floor; UI should mark these explicitly."
        ),
    )
    sanitized_value_paths: list[str] = Field(
        default_factory=list,
        description=(
            "JSONPath-ish locators ('$.suite[3].m.jax.r2') of non-finite floats the "
            "presentation sanitizer coerced to 0.0 when this artifact was written. "
            "Non-empty means some rendered 0.0 values are suppressions, not "
            "measurements (G5 disclosure, 2026-06-23 tribunal). Carried as a list so "
            "_sanitize cannot erase the signal; additive field — old payloads "
            "validate with []."
        ),
    )

    @model_validator(mode="after")
    def _harmonic_le_geomean(self) -> "ManifestSignals":
        """Invariant V (V3): HM ≤ GM for positive speedups (Eeckhout 2024).

        Promotes the previously documentation-only property to an enforced
        contract invariant — a payload where the harmonic mean exceeds the
        geometric mean is mathematically impossible and refuses to construct.
        """
        h = self.harmonic_mean_speedup_vs_baseline
        g = self.geomean_speedup_vs_baseline
        if h is not None and g > 0.0 and h > g * (1.0 + 1e-9):
            raise ValueError(
                f"harmonic mean {h} exceeds geometric mean {g} "
                "(impossible for positive speedups; AM ≥ GM ≥ HM)"
            )
        return self


