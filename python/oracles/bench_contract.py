"""Frozen data contract for the benchmark report (the ``BENCH`` payload).

This module is the **single source of truth** for the JSON the benchmark engine
emits and the ``web/`` React app consumes. Field names serialize to **camelCase**
via an alias generator, so the TypeScript types generated from this schema
(``web/src/contract.ts``) line up 1:1 with the React chart code.

Conventions
-----------
- Per-solver maps are ``dict[str, X]`` keyed by solver id (``"spectrafit"`` /
  ``"lmfit"`` / ``"jax"``).
- Peak parameters use the design's short keys: ``a`` (amplitude), ``c`` (center),
  ``s`` (sigma) — this is the *report* contract, distinct from the solver's
  canonical ``amplitude``/``center``/``sigma`` names.
- Convergence history carries a ``history_source`` map (``"real"`` |
  ``"reconstructed"``) so the UI can honestly label proxy curves.

Emit with ``model.model_dump(by_alias=True)``. The TypeScript types the web app
consumes (``web/src/contract.ts`` / ``web/src/openapi.gen.ts``) are generated by
``openapi-typescript`` from the OpenAPI schema the FastAPI app (:mod:`oracles.api`)
publishes for these same models — Pydantic is the single source of truth.
"""

