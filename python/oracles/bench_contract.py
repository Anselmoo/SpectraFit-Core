class SuiteMetric(_Base):
    """Single-point per-solver metrics for a suite row."""

    speedup: float
    r2: float
    red_chi2: float
    med_ms: float
    param_err: float
    success: bool

    # Track 1 additions — additive minor, all optional+default=None.
    convergence_efficiency: float | None = Field(
        default=None,
        description=(
            "Mean cost reduction per iteration: (cost₀ − costₙ) / n_iter. "
            "Populated when a real cost history exists (history_source=='real'); "
            "None for reconstructed histories. Additive field — old payloads "
            "validate with None."
        ),
    )
    ill_conditioned: bool | None = Field(
        default=None,
        description=(
            "True when κ(J) ≥ 1e6 (ill-conditioned). "
            "None when κ(J) was not computed. Additive field."
        ),
    )
    red_chi2_weighted: float | None = Field(
        default=None,
        description=(
            "σ-weighted reduced-χ²: Σ((y−ŷ)/σ)² / dof. "
            "None when σ=0 (noiseless case); see metric_undefined_reason. "
            "Additive field — old payloads validate with None."
        ),
    )
    metric_undefined_reason: str | None = Field(
        default=None,
        description=(
            "Human-readable explanation when red_chi2_weighted is None, "
            'e.g. "sigma=0 (noiseless)". Additive field.'
        ),
    )

    @field_validator("speedup", "r2", "red_chi2", "med_ms", "param_err", mode="after")
    @classmethod
    def _reject_non_finite_required(cls, v: float) -> float:
        """Required float metrics must be finite — no inf/NaN in the contract."""
        if not math.isfinite(v):
            raise ValueError(
                f"metric field must be finite (got {v!r}); "
                "use None + metric_undefined_reason for undefined metrics"
            )
        return v

    @field_validator("convergence_efficiency", "red_chi2_weighted", mode="after")
    @classmethod
    def _reject_non_finite_optional(cls, v: float | None) -> float | None:
        """Optional float metrics: finite when present, None otherwise."""
        if v is not None and not math.isfinite(v):
            raise ValueError(
                f"optional metric field must be finite or None (got {v!r})"
            )
        return v


