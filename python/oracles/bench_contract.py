class CalibrationResult(_Base):
    """Frozen contract view of ``oracles.inference.CalibrationStat``.

    Result of a binomial calibration test on parameter pulls
    (θ_est − θ_true) / σ_est. All fields mirror ``CalibrationStat`` exactly
    so the engine can pass the stat object straight into ``model_validate``.

    When fewer than ``InferenceConfig.min_pulls`` pulls were collected, the
    engine still emits a **populated** record with ``skipped=True, passed=False,
    coverage=0.0, binomial_p=1.0`` — it is never ``None`` for insufficient data.
    ``None`` on ``InferenceBlock`` only appears for old payloads that predate
    the field (additive-minor schema upgrade); the web layer must check
    ``skipped`` to distinguish "test ran but data was insufficient" from
    "old payload that never had the field".

    ``binomial_p`` is retained as an *honest strict diagnostic*: it reports the
    point-null test of H0: coverage = nominal.  At large n this has extreme power
    and will reject even a negligible deviation; it is NOT the gate criterion.

    ``passed`` uses a practical-equivalence rule: the Clopper–Pearson CI must lie
    entirely within ``[nominal − equivalence_margin, nominal + equivalence_margin]``.
    ``equivalence_margin`` carries the pre-registered band (default 0.03 = ±3 pp).
    Additive field — old payloads without it default to 0.03 (matching the engine
    default) so validation is lossless.
    """

    n: int
    coverage: float
    coverage_ci_lo: float
    coverage_ci_hi: float
    nominal: float
    binomial_p: float
    ks_stat: float
    ks_p: float
    alpha: float
    equivalence_margin: float = 0.03
    passed: bool
    skipped: bool


class SpeedInferenceResult(_Base):
    """Frozen contract view of ``oracles.inference.SpeedStat``.

    Result of a geomean-speedup significance test: bootstrap CI on the
    geometric mean of per-case speedup ratios plus sign-test and Wilcoxon
    signed-rank p-values. All fields mirror ``SpeedStat`` exactly.

    When fewer than 2 valid per-case speedups were available, the engine still
    emits a **populated** record with ``skipped=True, passed=False,
    geomean_speedup=0.0, ci_lo=0.0, ci_hi=0.0`` — it is never ``None`` for
    insufficient data. ``None`` on ``InferenceBlock`` only appears for old
    payloads that predate the field (additive-minor schema upgrade); the web
    layer must check ``skipped`` to distinguish "test ran but data was
    insufficient" from "old payload that never had the field".
    """

    geomean_speedup: float
    ci_lo: float
    ci_hi: float
    excludes_one: bool
    sign_p: float
    wilcoxon_p: float
    alpha: float
    passed: bool
    skipped: bool


class InferenceBlock(_Base):
    """Top-level inference attached to BenchReport (additive 1.3 -> 1.4).

    ``calibration`` and ``speed_inference`` are additive-minor fields (Task 5.3):
    old payloads that lack them validate with ``None``; the gate axis is inert
    when ``None`` (no-pass-by-absence rule).
    """

    config: InferenceConfig
    cases: list[CaseInference]
    equivalence: list[EquivalenceResult]
    winner_stability: dict[str, float]
    calibration: CalibrationResult | None = None
    speed_inference: SpeedInferenceResult | None = None


