class BenchReport(_Base):
    """The complete benchmark report payload (the ``BENCH`` object consumed by the web app)."""

    schema_version: str = SCHEMA_VERSION
    solvers: list[SolverMeta]
    categories: list[CategoryMeta]
    analyzed: list[Featured]
    suite: list[SuiteCase]
    # Solver id whose median runtime defines the speedup baseline (×1.0). Was
    # implicitly "lmfit" through hardcoded engine lookups (`by_name["lmfit"]`)
    # and a hardcoded web label; now an explicit contract field so adding a
    # fourth backend or retiring lmfit doesn't ripple across every consumer.
    # Additive 1.0 → 1.1 bump: old payloads without this field default here.
    baseline_solver_id: str = "lmfit"
    # Manifest headline numbers (mirrors `reports._headline`) — exposed so the
    # web gate-state UI can render geomean / max |Δr²| / win-rate / pinned
    # ratio without a sidecar fetch. Additive 1.1 → 1.2 bump; old payloads on disk
    # validate against the bumped schema because Pydantic fills the default.
    manifest: ManifestSignals | None = None
    trust_block: TrustBlock | None = Field(
        default=None,
        description=(
            "V&V evidence block emitted by oracles.audit.runner. Additive — "
            "Pydantic fills None for pre-Plan-E payloads, so old runs still parse."
        ),
    )
    # Declarative panel registry consumed by the web PanelRenderer. Additive
    # 1.2 -> 1.3 bump: old payloads validate with [] and the web app renders
    # its explicit "report predates panel specs" empty state (no silent
    # fallback to a hardcoded layout).
    panels: list[PanelSpec] = Field(default_factory=list)
    inference: InferenceBlock | None = None
    # Track 1 additions — code provenance captured at run start.
    git_commit: str | None = Field(
        default=None,
        description=(
            "Git commit hash (short, ≥7 chars) at benchmark run time. "
            "None when git is unavailable or the run is not in a git repo. "
            "Additive field — old payloads validate with None."
        ),
    )
    git_branch: str | None = Field(
        default=None,
        description=(
            "Git branch name at benchmark run time. "
            "None when git is unavailable. Additive field."
        ),
    )
    run_timestamp_unix: int | None = Field(
        default=None,
        description=(
            "Unix epoch seconds when the benchmark run started (int). "
            "None when not captured. Additive field."
        ),
    )
