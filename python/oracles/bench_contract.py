class Uncertainty(_Base):
    """Parameter-pull diagnostics: pulls = (est−truth)/σ, coverage, reported σ.

    ``coverage`` is ``None`` when no valid σ was available for any MC sample
    (e.g. backends like jax that set every ``param_stderr`` entry to ``None``).
    A ``None`` coverage is distinct from a genuine ``0.0`` (all pulls |p| ≥ 1σ).
    """

    pulls: list[float]
    coverage: float | None
    sigma: list[float]


class SpreadPt(_Base):
    """A mean±sd point versus run count (param-spread / stability series)."""

    n: int
    mean: float
    sd: float


class StabilityEntry(_Base):
    """One backend's metric stability versus run count (mean±sd per metric)."""

    r2: list[SpreadPt]
    rmse: list[SpreadPt]
    red_chi2: list[SpreadPt]
    iters: list[SpreadPt]


class NdPeak(_Base):
    """A recovered N-D Gaussian peak: amplitude + per-axis center and sigma.

    ``center`` and ``sigma`` each have ``n_dims`` entries (indexed `center_0..` /
    `sigma_0..` from the parametric ``gaussian_nd`` kernel).
    """

    amplitude: float
    center: list[float]
    sigma: list[float]


class Projection(_Base):
    """A 2-D projection (marginal slice) of a higher-D fit: axis-pair + matrix."""

    labels: tuple[str, str]
    matrix: list[list[float]]


class MultiDim(_Base):
    """Genuinely N-dimensional (≥3-D) fit showcase (SP-2).

    A synthetic N-D Gaussian is recovered by a real least-squares solve with the
    parametric ``gaussian_nd`` kernel (D inferred from the node's indexed
    ``center_<i>`` parameters). ``peaks`` are the *fitted* parameters and
    ``r_squared`` the recovery quality. Full N-D obs/model/residual grids are
    intentionally NOT stored — they do not scale past 2-D — so ``projections``
    carries 2-D marginal slices of the fitted model for any visualization.

    **SYNTHETIC — no experimental data; NOT rendered in the production UI
    (classified ``ignored: cut``).**
    """

    n_dims: int
    shape: list[int]
    n_points: int
    r_squared: float
    peaks: list[NdPeak]
    projections: list[Projection]
    source: str = "spectrafit-core"
    data_provenance: Literal["measured", "synthetic"] = "synthetic"


class GlobalFitSlice(_Base):
    """One dataset slice in a shared-model multi-spectrum global fit: observed + jointly-fitted model.

    The ``coord`` field carries the axis coordinate for this slice (e.g. a time
    value, a temperature, a sample index) — the name is axis-neutral because the
    global-fit machinery is not specific to time.
    """

    coord: float
    obs: list[float]
    model: list[float]


class PeakTrace(_Base):
    """A shared peak's amplitude evolving across the dataset axis."""

    label: str
    center: float
    sigma: float
    amplitude: list[float]


class GlobalFit(_Base):
    """Shared-model multi-spectrum joint global fit (the lmfit ``fit_multi_datasets`` pattern).

    **SYNTHETIC placeholder — no experimental data.**  Uses a ``GlobalFitGraph``
    fit over synthetic Gaussian peaks; this field is NOT rendered in the production
    UI (classified ``ignored: cut``).  Peak centers/widths are shared across every
    dataset slice (global analysis) while each slice's amplitudes vary; ``traces``
    are the recovered per-peak amplitude traces and ``slices`` carry the per-dataset
    observed + jointly-fitted curves on the shared ``x`` grid.

    The ``dataset_axis`` / ``axis_label`` names are deliberately axis-neutral: the
    global-fit machinery in spectrafit-core (``GlobalFitGraph``) is not specific to
    time — here the axis incidentally represents time, but the contract does not
    claim that.
    """

    x: list[float]
    dataset_axis: list[float]
    slices: list[GlobalFitSlice]
    traces: list[PeakTrace]
    source: str = "spectrafit-core"
    x_label: str = "x"
    axis_label: str = "dataset"
    data_provenance: Literal["measured", "synthetic"] = "synthetic"


class BackendProfile(_Base):
    """All of ONE backend's featured-case metrics, grouped (no parallel dicts).

    Element types match the per-panel charts exactly: `scaling`/`ecdf_*` keep the
    (x, y) `Point2D` axes, `param_err` is per-parameter, `param_spread`/`stability`
    keep the mean±sd `SpreadPt` bands.
    """

    fit: SolverFit
    conv: list[float]
    grad: list[float]
    conv_eff: list[float] | None = Field(
        default=None,
        description=(
            "Per-iteration convergence efficiency (cost reduction per step) derived "
            "from `conv`. Populated ONLY when `history_source == 'real'` (the subject's "
            "measured cost history); `None` for backends whose history is "
            "`reconstructed`, since a proxy-derived efficiency must not be presented "
            "as a measured series. Additive field — old payloads validate with None."
        ),
    )
    history_source: Literal["real", "reconstructed"]
    timing: TimingDist
    accuracy: AccuracyDist
    summary: Summary
    param_err: list[float]
    ecdf_resid: list[Point2D]
    ecdf_time: list[Point2D]
    warmup: Warmup
    scaling: list[Point2D]
    uncertainty: Uncertainty
    param_spread: list[SpreadPt]
    stability: StabilityEntry
    jacobian_condition_number: float | None = Field(
        default=None,
        description=(
            "κ(J) at convergence. Structural property of the case, not the solver. "
            "κ ≥ 1e6 → ill-conditioned (explains backend disagreement); κ ≤ 1e4 → well-posed."
        ),
    )
    theta_distance: list[float] | None = Field(
        default=None,
        description=(
            "Scale-normalized per-iteration distance of the parameter vector to the "
            "synthetic ground truth, dₖ = ‖(θₖ − θ_true)/s‖₂ (s = per-parameter "
            "magnitude scale). Same length as `conv`. The REAL convergence-to-truth "
            "metric — distinct from the χ² descent in `conv`. None for non-synthetic "
            "cases (no θ_true) or backends without a parameter trajectory (only "
            "spectrafit's faer LM records it). Additive field — old payloads validate "
            "with None."
        ),
    )


