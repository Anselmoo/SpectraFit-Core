class SelectionStats(_Base):
    """Frozen contract view of ``oracles.nested.SelectionStats``.

    Statistics for one nested-model comparison (full minus reduced):
    LRT / F-test / information criteria.  All statistics are "full minus
    reduced" oriented: negative Δ-AIC / Δ-BIC means the full model is
    preferred.
    """

    lrt_stat: float
    lrt_p: float
    f_stat: float
    f_p: float
    d_aic: float = Field(serialization_alias="dAIC", validation_alias="dAIC")
    d_bic: float = Field(serialization_alias="dBIC", validation_alias="dBIC")


class NestedAdequacy(_Base):
    """Frozen contract view of ``oracles.nested.NestedAdequacy``.

    Result of a nested-order V&V against a known generative order: checks
    that model-selection criteria (LRT / F / AIC / BIC) recover the true
    order m* by comparing the (m*−1) reduced, m* true, and (m*+1)
    over-fitted models.

    Both AIC and BIC verdicts are reported separately.  On the featured
    real tri-Gaussian, AIC over-selects the 4-peak model
    (``over_not_preferred_aic = False``) while BIC correctly recovers the
    true 3-peak order (``over_not_preferred_bic = True``).

    ``nested_adequacy: NestedAdequacy | None = None`` on ``Featured`` is an
    additive-minor field — old payloads that lack it validate with ``None``.
    """

    true_order: int
    reduced_rejected: bool
    over_not_preferred_aic: bool
    over_not_preferred_bic: bool
    selected_order_aic: int
    selected_order_bic: int
    recovered_true_order_aic: bool
    recovered_true_order_bic: bool
    reduced_vs_true: SelectionStats
    true_vs_over: SelectionStats


